package org.example;

public class Calculation {

    public int plus(int fst, int snd){
        return fst + snd;
    }

    public int subtract(int fst, int snd){
        return fst - snd;
    }

    public int multiply(int fst, int snd){
        return fst * snd;
    }

    public int division(int fst, int snd){
        return fst / snd;
    }

}
